package com.example;

import org.springframework.data.annotation.Id;

public class User {
    @Id
    private String name;

    public User(String name) {
        this.name = name;
    }

    public User() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
